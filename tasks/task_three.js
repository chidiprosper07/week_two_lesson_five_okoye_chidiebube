// Create an array with multiple elements
let myArray = ["Element 1", "Element 2", "Element 3"];

// Access a specific element using array indexing
let index = 1; // Index of the element to modify
var elementToModify = myArray[index];

// Modify the element value using assignment
elementToModify = "Modified Element";

// Assign the modified element back to the array
myArray[index] = elementToModify;

// Log the array to the console
console.log(myArray);
